"""Generate and sync MedHallu summary snippets from results/medhallu_results.json.

This script avoids editing removed legacy files and only touches files that
exist in the current repository layout.
"""

from __future__ import annotations

import json
from pathlib import Path
from datetime import datetime

ROOT = Path(__file__).resolve().parents[1]
MED_PATH = ROOT / "results" / "medhallu_results.json"
README_PATH = ROOT / "README.md"
COMPREHENSIVE_PATH = ROOT / "comprehensive_results.md"
SUMMARY_OUT = ROOT / "results" / "medhallu_latest_summary.md"


def load_medhallu():
    payload = json.loads(MED_PATH.read_text(encoding="utf-8"))
    by_label = {r["label"]: r for r in payload["results"]}
    return payload, by_label


def pct(x: float) -> str:
    return f"{100.0 * float(x):.1f}%"


def build_summary_markdown(payload: dict, by_label: dict[str, dict]) -> str:
    stamp = datetime.fromtimestamp(MED_PATH.stat().st_mtime).strftime("%Y-%m-%d %H:%M:%S")
    lines = [
        "# MedHallu Latest Summary",
        "",
        f"Source: {MED_PATH.relative_to(ROOT)}",
        f"Last written: {stamp}",
        f"Runtime (min): {payload.get('runtime_min', 'n/a')}",
        "",
        "| method | accuracy | rep_rate |",
        "|---|---:|---:|",
    ]

    for label in sorted(by_label):
        row = by_label[label]
        lines.append(f"| {label} | {pct(row.get('accuracy', 0.0))} | {pct(row.get('rep_rate', 0.0))} |")

    lines.append("")
    lines.append("Generated by experiments/sync_docs_from_medhallu.py")
    return "\n".join(lines) + "\n"


def maybe_update_comprehensive_timestamp() -> bool:
    if not COMPREHENSIVE_PATH.exists():
        return False

    text = COMPREHENSIVE_PATH.read_text(encoding="utf-8")
    today = datetime.now().strftime("%Y-%m-%d")
    markers = ["Last update:", "Last sync:"]
    marker = next((m for m in markers if m in text), None)
    if marker is None:
        return False

    updated = []
    changed = False
    for line in text.splitlines():
        if line.startswith(marker):
            updated.append(f"{marker} {today}")
            changed = True
        else:
            updated.append(line)

    if changed:
        COMPREHENSIVE_PATH.write_text("\n".join(updated) + "\n", encoding="utf-8")
    return changed


def main() -> None:
    payload, by_label = load_medhallu()
    summary_md = build_summary_markdown(payload, by_label)
    SUMMARY_OUT.write_text(summary_md, encoding="utf-8")

    touched = [str(SUMMARY_OUT.relative_to(ROOT))]
    if maybe_update_comprehensive_timestamp():
        touched.append(str(COMPREHENSIVE_PATH.relative_to(ROOT)))

    if README_PATH.exists():
        # Keep README update behavior lightweight and non-destructive.
        readme = README_PATH.read_text(encoding="utf-8")
        needle = "- For current status narrative, use comprehensive_results.md."
        extra = "- Generated MedHallu quick summary: results/medhallu_latest_summary.md"
        if needle in readme and extra not in readme:
            README_PATH.write_text(readme.replace(needle, needle + "\n" + extra), encoding="utf-8")
            touched.append(str(README_PATH.relative_to(ROOT)))

    print("Updated files:")
    for item in touched:
        print(f"- {item}")


if __name__ == "__main__":
    main()
